//go:build ignore

// Command generate-font-symbol-classes emits the checked-in, versioned symbol
// taxonomy used by font rules. The source ranges below are deliberately local:
// regeneration is deterministic and never depends on the network.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"os"
)

const outputPath = "internal/fontdesc/classes_generated.go"

type classBit uint16

const (
	classEmoji classBit = 1 << iota
	classCJK
	classNerdFont
	classPowerline
	classBoxDrawing
	classBraille
	classSymbols
	classDefaultIgnorable
)

type sourceRange struct {
	lo, hi rune
	class  classBit
}

func main() {
	check := flag.Bool("check", false, "verify the checked-in generated file")
	flag.Parse()
	generated, err := generate()
	if err != nil {
		fatal(err)
	}
	if *check {
		current, err := os.ReadFile(outputPath)
		if err != nil {
			fatal(err)
		}
		// Git's Windows checkout may materialize the generated source with CRLF,
		// while go/format deterministically emits LF. Compare canonical bytes.
		current = bytes.ReplaceAll(current, []byte("\r\n"), []byte("\n"))
		if !bytes.Equal(current, generated) {
			fatal(fmt.Errorf("%s is stale; run go run ./scripts/generate-font-symbol-classes.go", outputPath))
		}
		fmt.Println("font symbol classes are current")
		return
	}
	if err := os.WriteFile(outputPath, generated, 0o644); err != nil {
		fatal(err)
	}
	fmt.Printf("wrote %s\n", outputPath)
}

func generate() ([]byte, error) {
	ranges := sourceRanges()
	classes := []struct {
		bit  classBit
		name string
	}{
		{classEmoji, "Emoji"}, {classCJK, "CJK"}, {classNerdFont, "NerdFont"}, {classPowerline, "Powerline"},
		{classBoxDrawing, "BoxDrawing"}, {classBraille, "Braille"}, {classSymbols, "Symbols"}, {classDefaultIgnorable, "DefaultIgnorable"},
	}
	grouped := make(map[classBit][]sourceRange, len(classes))
	for _, item := range ranges {
		if item.lo < 0 || item.hi > 0x10ffff || item.lo > item.hi {
			return nil, fmt.Errorf("invalid source range U+%04X..U+%04X", item.lo, item.hi)
		}
		grouped[item.class] = append(grouped[item.class], item)
	}
	for _, class := range classes {
		items := grouped[class.bit]
		for index := 1; index < len(items); index++ {
			if items[index].lo <= items[index-1].hi {
				return nil, fmt.Errorf("overlapping %s source ranges at U+%04X", class.name, items[index].lo)
			}
		}
	}

	var source bytes.Buffer
	fmt.Fprintln(&source, "// Code generated by scripts/generate-font-symbol-classes.go; DO NOT EDIT.")
	fmt.Fprintln(&source, "// Unicode 15.1.0 exact properties/scripts/categories; Nerd Fonts 3.3.0 allocated manifest.")
	fmt.Fprintln(&source, "package fontdesc")
	fmt.Fprintln(&source)
	fmt.Fprintln(&source, `const FontSymbolClassDataVersion = "unicode-15.1.0+nerd-fonts-3.3.0"`)
	fmt.Fprintln(&source, `const FontSymbolClassSourceSHA256 = "emoji:d7aef489c8fe4c14f09ea5695200277c6b93ac82ac60845cdd2161b0d6835cc1;scripts:0eacb65169ae6eb1d399cd70826b3da15fff19f6f586eecf819b70c83b1d9b32+fdfd54237a2c0452ba1060571fd1e58fd46aeecdfda7c5b5be1b716dad755cec;unicode-data:2fc713e6a31a87c4850a37fe2caffa4218180fadb5de86b43a143ddb4581fb86;ignorables:f55d0db69123431a7317868725b1fcbf1eab6b265d756d1bd7f0f6d9f9ee108b;nerd:5a5eb3ffec41b36eed8bf3b7a1f6bbdf3f2f54cbd3821e98b530a4c5e0ec2f97"`)
	for _, class := range classes {
		fmt.Fprintf(&source, "\nvar generated%sRanges = [...]rune{\n", class.name)
		for index, item := range grouped[class.bit] {
			if index%4 == 0 {
				fmt.Fprint(&source, "\t")
			}
			fmt.Fprintf(&source, "0x%X, 0x%X, ", item.lo, item.hi)
			if index%4 == 3 || index == len(grouped[class.bit])-1 {
				fmt.Fprintln(&source)
			}
		}
		fmt.Fprintln(&source, "}")
	}
	fmt.Fprintln(&source, `

func generatedRangeContains(r rune, ranges []rune) bool {
	low, high := 0, len(ranges)/2
	for low < high {
		middle := low + (high-low)/2
		lo, hi := ranges[middle*2], ranges[middle*2+1]
		if r < lo { high = middle; continue }
		if r > hi { low = middle + 1; continue }
		return true
	}
	return false
}`)
	return format.Source(source.Bytes())
}


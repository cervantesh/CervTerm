package theme

import "fmt"

type Color struct {
	R, G, B uint8
}

func (c Color) Hex() string {
	return fmt.Sprintf("#%02X%02X%02X", c.R, c.G, c.B)
}

type Palette struct {
	Name       string
	Background Color
	Surface    Color
	Chrome     Color
	Foreground Color
	Muted      Color
	Accent     Color
	ANSI       [16]Color
}

func DefaultPalette() Palette {
	return Palette{
		Name:       "CervTerm dark",
		Background: Color{0x08, 0x0B, 0x12},
		Surface:    Color{0x10, 0x15, 0x20},
		Chrome:     Color{0x13, 0x1B, 0x2A},
		Foreground: Color{0xE6, 0xE1, 0xD8},
		Muted:      Color{0xA8, 0xB3, 0xC7},
		Accent:     Color{0x60, 0xE8, 0xF0},
		ANSI: [16]Color{
			{0x1B, 0x22, 0x32}, {0xFF, 0x5C, 0x8A}, {0x8B, 0xF5, 0x9A}, {0xF8, 0xD8, 0x66},
			{0x7A, 0xA2, 0xFF}, {0xD8, 0x8C, 0xFF}, {0x60, 0xE8, 0xF0}, {0xD8, 0xDE, 0xEA},
			{0x57, 0x62, 0x7A}, {0xFF, 0x7A, 0xA8}, {0xA6, 0xFF, 0xB5}, {0xFF, 0xE6, 0x8A},
			{0x9B, 0xB8, 0xFF}, {0xE5, 0xA7, 0xFF}, {0x90, 0xF4, 0xFF}, {0xFF, 0xFF, 0xFF},
		},
	}
}
